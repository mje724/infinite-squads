EYES={"normal","shades","wink","angry","wide","sleepy","smug","intense","dot"}
MOUTH={"smile","grin","smirk","frown","neutral","open","smug"}
def av(bg, skin, hair, hairColor, facial, eyes, mouth, prop):
    assert hair in HAIR, "bad hair: "+hair
    assert facial in FACIAL, "bad facial: "+facial
    assert eyes in EYES, "bad eyes: "+eyes
    assert mouth in MOUTH, "bad mouth: "+mouth
    return dict(bg=bg, skin=skin, hair=hair, hairColor=hairColor, facial=facial, eyes=eyes, mouth=mouth, prop=prop)

