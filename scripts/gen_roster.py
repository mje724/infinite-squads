def q(name, nick, era, tags, rarity, prop, theme):
    raw = hashlib.sha256(name.encode()).digest()
    core = [45 + (raw[i] % 15) for i in range(6)]
    for tag in tags:
        core = [v + d for v,d in zip(core, TAG_BOOSTS[tag])]
    core = [max(1, min(99, v)) for v in core]
    bases = [88, 28, 76, 61, 84, 72]
    stats = [[label, max(0,min(100, base + (raw[i+6] % 21) - 10)), emoji]
             for i,((label,emoji),base) in enumerate(zip(THEMES[theme],bases))]
    bg,skin,hair,hairColor = PALETTES[raw[12] % len(PALETTES)]
    if "animal" in tags or "entity" in tags or "cryptid" in tags: hair = "blob"
    facial = ["none","stubble","goatee","full"][raw[13] % 4] if hair != "blob" else "none"
    eyes = ["normal","wink","smug","intense","wide"][raw[14] % 5]
    mouth = ["smile","grin","smirk","neutral","frown"][raw[15] % 5]
    return c(name,nick,era,tags,core,stats,rarity,av(bg,skin,hair,hairColor,facial,eyes,mouth,prop))

