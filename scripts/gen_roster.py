def emit():
    cards = [x for b in BATCHES for x in b]
    # collision guard
    existing = set(json.load(open('scripts/existing_names.json')))
    seen = set()
    for card in cards:
        if card['name'] in existing:
            print("COLLISION with existing:", card['name']); sys.exit(1)
        if card['name'] in seen:
            print("DUPLICATE in roster:", card['name']); sys.exit(1)
        seen.add(card['name'])
    J = json.dumps
    lines = []
    lines.append("// AUTO-GENERATED by scripts/gen_roster.py — DO NOT EDIT BY HAND.")
    lines.append("// Single source of truth for the expansion roster: one object per card,")
    lines.append("// emitted into presetCards + cardRegistry + avatar with zero desync.")
    lines.append("import type { Era, Tag, CardGameData, CoreStats } from '@/data/cardRegistry';")
    lines.append("import type { AvatarTraits } from '@/lib/avatar';")
    lines.append("import type { PresetCard } from '@/data/presetCards';")
    lines.append("")
    lines.append("export interface RosterCard {")
    lines.append("  name: string; nickname: string; era: Era; tags: Tag[];")
    lines.append("  core: CoreStats; stats: { label: string; value: number; emoji: string }[];")
    lines.append("  rarity: PresetCard['rarity']; avatar: AvatarTraits;")
    lines.append("}")
    lines.append("")
    lines.append("function c(name: string, nickname: string, era: Era, tags: Tag[], core: [number,number,number,number,number,number], stats: [string, number, string][], rarity: PresetCard['rarity'], avatar: AvatarTraits): RosterCard {")
    lines.append("  return { name, nickname, era, tags, core: { might: core[0], brains: core[1], charm: core[2], chaos: core[3], grit: core[4], clout: core[5] }, stats: stats.map(([label, value, emoji]) => ({ label, value, emoji })), rarity, avatar };")
    lines.append("}")
    lines.append("")
    lines.append("export const ROSTER: RosterCard[] = [")
    for card in cards:
        core = ",".join(str(int(x)) for x in card['core'])
        stats = ", ".join("[%s, %d, %s]" % (J(s[0]), int(s[1]), J(s[2])) for s in card['stats'])
        a = card['av']
        avs = "{ bg: %s, skin: %s, hair: %s, hairColor: %s, facial: %s, eyes: %s, mouth: %s, prop: %s }" % (
            J(a['bg']), J(a['skin']), J(a['hair']), J(a['hairColor']), J(a['facial']), J(a['eyes']), J(a['mouth']), J(a['prop']))
        tags = "[" + ", ".join(J(t) for t in card['tags']) + "]"
        lines.append("  c(%s, %s, %s, %s, [%s], [%s], %s, %s)," % (
            J(card['name']), J(card['nick']), J(card['era']), tags, core, stats, J(card['rarity']), avs))
    lines.append("];")
    lines.append("")
    lines.append("export const ROSTER_REGISTRY: Record<string, CardGameData> = Object.fromEntries(ROSTER.map(c => [c.name, { era: c.era, tags: c.tags, core: c.core }]));")
    lines.append("export const ROSTER_AVATARS: Record<string, AvatarTraits> = Object.fromEntries(ROSTER.map(c => [c.name, c.avatar]));")
    lines.append("export const ROSTER_PRESET_BASE: Omit<PresetCard, 'image'>[] = ROSTER.map(c => ({ name: c.name, nickname: c.nickname, stats: c.stats, rarity: c.rarity }));")
    lines.append("")
    open('src/data/megaRoster.ts','w').write("\n".join(lines))
    print("emitted", len(cards), "roster cards")

if __name__ == '__main__':
    emit()
