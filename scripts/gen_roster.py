# c(name, nick, era, tags, core[mi,br,ch,cx,gr,cl], stats[[label,val,emoji]x6], rarity, av{...})
def c(name, nick, era, tags, core, stats, rarity, av):
    assert len(core) == 6, name
    assert len(stats) == 6, name
    for label, val, emoji in stats:
        assert 0 <= val <= 100, (name, label, val)
    for k in core:
        assert 1 <= k <= 99, (name, k)
    return dict(name=name, nick=nick, era=era, tags=tags, core=core, stats=stats, rarity=rarity, av=av)

